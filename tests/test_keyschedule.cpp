#include <gtest/gtest.h>

#include <cstdint>

#include "KeySchedule.h"
#include "Mapper.h"

TEST(TestKeySchedule, KS_Test) {
  uint64_t key =
      0b0001001100110100010101110111100110011011101111001101111111110001;
  uint64_t keys[16] = {0};
  KeySchedule::generate_schedule(key, keys);

  ASSERT_EQ(0b000110110000001011101111111111000111000001110010, keys[0]);
  ASSERT_EQ(0b011110011010111011011001110110111100100111100101, keys[1]);
  ASSERT_EQ(0b010101011111110010001010010000101100111110011001, keys[2]);
  ASSERT_EQ(0b011100101010110111010110110110110011010100011101, keys[3]);
  ASSERT_EQ(0b011111001110110000000111111010110101001110101000, keys[4]);
  ASSERT_EQ(0b011000111010010100111110010100000111101100101111, keys[5]);
  ASSERT_EQ(0b111011001000010010110111111101100001100010111100, keys[6]);
  ASSERT_EQ(0b111101111000101000111010110000010011101111111011, keys[7]);
  ASSERT_EQ(0b111000001101101111101011111011011110011110000001, keys[8]);
  ASSERT_EQ(0b101100011111001101000111101110100100011001001111, keys[9]);
  ASSERT_EQ(0b001000010101111111010011110111101101001110000110, keys[10]);
  ASSERT_EQ(0b011101010111000111110101100101000110011111101001, keys[11]);
  ASSERT_EQ(0b100101111100010111010001111110101011101001000001, keys[12]);
  ASSERT_EQ(0b010111110100001110110111111100101110011100111010, keys[13]);
  ASSERT_EQ(0b101111111001000110001101001111010011111100001010, keys[14]);
  ASSERT_EQ(0b110010110011110110001011000011100001011111110101, keys[15]);
}