#include <gtest/gtest.h>

#include <cstdint>

#include "KeySchedule.h"
#include "Mapper.h"

TEST(TestKeySchedule, KS_TestPC1) {
  uint64_t key =
      0b0001001100110100010101110111100110011011101111001101111111110001;
  KeySchedule::generate_schedule(key);

  ASSERT_EQ(key, KeySchedule::get_key());
  ASSERT_EQ(Mapper::apply_map(key, PC1), KeySchedule::get_permuted_key());
}

// Get values from:
// https://page.math.tu-berlin.de/~kant/teaching/hess/krypto-ws2006/des.htm
TEST(TestKeySchedule, KS_TestKeyBlocks) {
  uint64_t key =
      0b0001001100110100010101110111100110011011101111001101111111110001;
  KeySchedule::generate_schedule(key);

  ASSERT_EQ(0b1111000011001100101010101111, KeySchedule::get_C(0));
  ASSERT_EQ(0b0101010101100110011110001111, KeySchedule::get_D(0));

  ASSERT_EQ(0b1110000110011001010101011111, KeySchedule::get_C(1));
  ASSERT_EQ(0b1010101011001100111100011110, KeySchedule::get_D(1));

  ASSERT_EQ(0b1100001100110010101010111111, KeySchedule::get_C(2));
  ASSERT_EQ(0b0101010110011001111000111101, KeySchedule::get_D(2));

  ASSERT_EQ(0b0000110011001010101011111111, KeySchedule::get_C(3));
  ASSERT_EQ(0b0101011001100111100011110101, KeySchedule::get_D(3));

  ASSERT_EQ(0b0011001100101010101111111100, KeySchedule::get_C(4));
  ASSERT_EQ(0b0101100110011110001111010101, KeySchedule::get_D(4));

  ASSERT_EQ(0b1100110010101010111111110000, KeySchedule::get_C(5));
  ASSERT_EQ(0b0110011001111000111101010101, KeySchedule::get_D(5));

  ASSERT_EQ(0b0011001010101011111111000011, KeySchedule::get_C(6));
  ASSERT_EQ(0b1001100111100011110101010101, KeySchedule::get_D(6));

  ASSERT_EQ(0b1100101010101111111100001100, KeySchedule::get_C(7));
  ASSERT_EQ(0b0110011110001111010101010110, KeySchedule::get_D(7));

  ASSERT_EQ(0b0010101010111111110000110011, KeySchedule::get_C(8));
  ASSERT_EQ(0b1001111000111101010101011001, KeySchedule::get_D(8));

  ASSERT_EQ(0b0101010101111111100001100110, KeySchedule::get_C(9));
  ASSERT_EQ(0b0011110001111010101010110011, KeySchedule::get_D(9));

  ASSERT_EQ(0b0101010111111110000110011001, KeySchedule::get_C(10));
  ASSERT_EQ(0b1111000111101010101011001100, KeySchedule::get_D(10));

  ASSERT_EQ(0b0101011111111000011001100101, KeySchedule::get_C(11));
  ASSERT_EQ(0b1100011110101010101100110011, KeySchedule::get_D(11));

  ASSERT_EQ(0b0101111111100001100110010101, KeySchedule::get_C(12));
  ASSERT_EQ(0b0001111010101010110011001111, KeySchedule::get_D(12));

  ASSERT_EQ(0b0111111110000110011001010101, KeySchedule::get_C(13));
  ASSERT_EQ(0b0111101010101011001100111100, KeySchedule::get_D(13));

  ASSERT_EQ(0b1111111000011001100101010101, KeySchedule::get_C(14));
  ASSERT_EQ(0b1110101010101100110011110001, KeySchedule::get_D(14));

  ASSERT_EQ(0b1111100001100110010101010111, KeySchedule::get_C(15));
  ASSERT_EQ(0b1010101010110011001111000111, KeySchedule::get_D(15));

  ASSERT_EQ(0b1111000011001100101010101111, KeySchedule::get_C(16));
  ASSERT_EQ(0b0101010101100110011110001111, KeySchedule::get_D(16));
}

TEST(TestKeySchedule, KS_TestPC2) {
  uint64_t key =
      0b0001001100110100010101110111100110011011101111001101111111110001;
  KeySchedule::generate_schedule(key);

  ASSERT_EQ(0b000110110000001011101111111111000111000001110010,
            KeySchedule::get_K(0));
  ASSERT_EQ(0b011110011010111011011001110110111100100111100101,
            KeySchedule::get_K(1));
  ASSERT_EQ(0b010101011111110010001010010000101100111110011001,
            KeySchedule::get_K(2));
  ASSERT_EQ(0b011100101010110111010110110110110011010100011101,
            KeySchedule::get_K(3));
  ASSERT_EQ(0b011111001110110000000111111010110101001110101000,
            KeySchedule::get_K(4));
  ASSERT_EQ(0b011000111010010100111110010100000111101100101111,
            KeySchedule::get_K(5));
  ASSERT_EQ(0b111011001000010010110111111101100001100010111100,
            KeySchedule::get_K(6));
  ASSERT_EQ(0b111101111000101000111010110000010011101111111011,
            KeySchedule::get_K(7));
  ASSERT_EQ(0b111000001101101111101011111011011110011110000001,
            KeySchedule::get_K(8));
  ASSERT_EQ(0b101100011111001101000111101110100100011001001111,
            KeySchedule::get_K(9));
  ASSERT_EQ(0b001000010101111111010011110111101101001110000110,
            KeySchedule::get_K(10));
  ASSERT_EQ(0b011101010111000111110101100101000110011111101001,
            KeySchedule::get_K(11));
  ASSERT_EQ(0b100101111100010111010001111110101011101001000001,
            KeySchedule::get_K(12));
  ASSERT_EQ(0b010111110100001110110111111100101110011100111010,
            KeySchedule::get_K(13));
  ASSERT_EQ(0b101111111001000110001101001111010011111100001010,
            KeySchedule::get_K(14));
  ASSERT_EQ(0b110010110011110110001011000011100001011111110101,
            KeySchedule::get_K(15));
}